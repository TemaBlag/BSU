.MODEL TINY
.CODE
ORG 100H

START:
 MOV BX,OFFSET FILE_NAME
 CALL COPY_FILE_NAME

  MOV DX,OFFSET FILE_NAME
  CALL DELETE_FILE
  MOV AX,4C00H
  INT 21H

FILE_NAME DB 256 DUP(?)


COPY_FILE_NAME PROC NEAR ; DS:BX - WHERE TO COPY FILE NAME
 PUSH ES
 MOV AX,ES:[2CH]
 MOV ES,AX
 MOV SI,-1

SEARCH_01:
 INC SI
 MOV AL,ES:[SI]
 CMP AL,0
 JNE SEARCH_01
 MOV AL,ES:[SI+1]
 CMP AL,1
 JNE SEARCH_01

 ADD SI,2

COPY_NAME:
 INC SI
 MOV AL,ES:[SI]
 MOV [BX],AL
 INC BX
 CMP AL,0
 JNE COPY_NAME

 POP ES
 RET
COPY_FILE_NAME ENDP


DELETE_FILE PROC NEAR ; DS:DX - FILE NAME
 MOV AH,41H
 INT 21H
 RET
DELETE_FILE ENDP
END START
